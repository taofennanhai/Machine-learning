def prepareDataSet(filepath):
    xy = np.genfromtxt(filepath, dtype=str)
    labels = xy[:, [-1]]
    labels.reshape(1, labels.shape[0])
    label = []
    for i, data in enumerate(labels, 0):
        label.append(data.item())

    feature = xy[:, 0:3]
    feature = feature.astype(np.float32)

    return feature, label


