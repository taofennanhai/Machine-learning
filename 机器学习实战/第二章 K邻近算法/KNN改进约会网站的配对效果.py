def autoNorm(dataSet):
    minVals = dataSet.min(0)
    maxVals = dataSet.max(0)
    ranges = maxVals- minVals
    m = dataSet.shape[0]
    dataSet = dataSet - np.tile(minVals, (m, 1))
    dataSet = dataSet / np.tile(ranges, (m, 1))

    return dataSet, ranges, minVals


